char *s21_strerror(int errnum) {
  static char unknown_error_message[50];
  char *result = (char *)s21_error_messages[errnum];
  if (errnum < 0 || errnum > (int)MAX_ERROR_CODE) {
    s21_sprintf(unknown_error_message, "Unknown error %d", errnum);
    result = unknown_error_message;
  }
  return result;
}